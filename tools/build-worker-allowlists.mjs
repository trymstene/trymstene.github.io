// GENERATE worker-rave's outfit id allowlists from the single-source manifest
// (src/data/wearables.js) — the worker bundle can't import site source, and
// the hand-pasted mirror kept threatening to drift. Now the manifest is law.
//
//   node tools/build-worker-allowlists.mjs
//
// Rewrites the block between ALLOWLISTS-START/END in worker-rave/src/index.js,
// then you deploy the worker. Part of the NEW-WEARABLE checklist.
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { WEARABLE_PACKS } from '../src/data/wearables.js';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const packs = Object.values(WEARABLE_PACKS);

const hats = ['none', ...packs.flatMap((p) => p.hats || []).map((d) => d.id)];
const shades = ['none', ...packs.flatMap((p) => p.shades || []).map((d) => d.id)];
// raveOnly items (beer/cone/vinyl/broom) are server-granted — excluded so
// sanitize strips them from client outfits
const extras = packs.flatMap((p) => p.extras || []).filter((d) => !d.raveOnly).map((d) => d.id);
const drops = packs.flatMap((p) => [...(p.hats || []), ...(p.shades || []), ...(p.extras || [])])
  .filter((d) => d.drop).map((d) => d.id);

const q = (a) => '[' + a.map((s) => `'${s}'`).join(', ') + ']';
const block = [
  '// ALLOWLISTS-START — GENERATED by tools/build-worker-allowlists.mjs from',
  '// src/data/wearables.js (the single-source manifest). NEVER edit by hand:',
  '// rerun the script after any wearable change, then deploy this worker.',
  '// raveOnly items (beer/cone/vinyl/broom) are server-granted and excluded',
  '// so sanitize strips them from client outfits.',
  `const HAT_IDS = ${q(hats)};`,
  `const DROP_IDS = ${q(drops)};`,
  `const SHADE_IDS = ${q(shades)};`,
  `const EXTRA_IDS = ${q(extras)};`,
  '// ALLOWLISTS-END',
].join('\n');

const workerPath = join(root, 'worker-rave', 'src', 'index.js');
const src = readFileSync(workerPath, 'utf8');
const re = /\/\/ ALLOWLISTS-START[\s\S]*?\/\/ ALLOWLISTS-END/;
if (!re.test(src)) {
  console.error('ALLOWLISTS markers not found in worker-rave/src/index.js');
  process.exit(1);
}
const next = src.replace(re, block);
const changed = next !== src;
writeFileSync(workerPath, next);
console.log(`hats ${hats.length} · drops ${drops.length} · shades ${shades.length} · extras ${extras.length} → ${changed ? 'worker allowlists UPDATED (deploy worker-rave)' : 'already in sync'}`);
